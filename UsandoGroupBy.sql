SELECT CODIGO, MAX(ID) AS ID FROM @TABELA GROUP BY CODIGO
--Resultado
--CODIGO	ID
--000001	1
--000002	2
--000003	4
--000004	5

SELECT 
	   P.ID,
       P.CODIGO,
	   P.DESCRICAO,
	   P.PRODUTO
FROM
(
    SELECT 
		CODIGO,
		MAX(ID) AS ID
	FROM
		@TABELA
	GROUP BY CODIGO
) D
INNER JOIN @TABELA P ON P.CODIGO = D.CODIGO AND P.ID = D.ID
WHERE 
	P.PRODUTO LIKE '%000002;%'
ORDER BY CODIGO
